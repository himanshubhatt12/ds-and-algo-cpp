#include<bits/stdc++.h>
using namespace std;

int main()
{
	// double exp (double x);
	int n = 2;
	cout<<exp(2)<<endl;

	//  logarithm
	//  double log (double x);
	int x = 5;
	cout<<log(x)<<endl;

	double y = 30.4;
	cout<<log(y)<<endl;

	//  logarithm to base 10
	//  double log10 (double x);
	cout<<log10(x)<<endl;
	cout<<log10(y)<<endl;

	//  logarithm to base 2
	//  double log2 (double x);
	cout<<log2(x)<<endl;
	cout<<log2(y)<<endl;
	
	return 0;
}
